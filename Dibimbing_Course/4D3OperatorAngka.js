// + (Addition)
console.log(5+9);
// - (Subtraction)
console.log(10-7);
// Multiplication
console.log(7*8);
// Exponentiation
console.log(7**2);
// / (Division)
console.log(8/2);
// % (Modulus)
console.log(10%2);
// ++ (Increment) 
let a = 8;
let b = a++; // prefix, mengembalikan nilai sesudah di increment
let c = ++a; //postfix, mengembalikan nilai sebelum di increment
console.log(a);
console.log(b);
console.log(c);
// -- (Decrement)
let d = 8;
let e = d--;
let f = --d;
console.log(d);
console.log(e);
console.log(f);